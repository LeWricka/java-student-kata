package com.upna.kata0.v1;

/**
 *
 * @author lewricka
 */
public class Student {
    private String name;
    private double score;

    public Student(String name, double score) {
        this.name = name;
        this.score = score;
    }
    
    public String getName() {
        return name;
    }

    public double getScore() {
        return score;
    }
}




